def detect_from_moves(game: chess.pgn.Game) -> str:
    """Walk up to 18 half-moves and return the best opening match."""
    board = game.board()
    sans: list[str] = []
    for move in game.mainline_moves():
        sans.append(board.san(move))
        board.push(move)
        if len(sans) >= 18:
            break
    move_str = " ".join(sans)
    for prefix, name in _MOVE_TABLE:
        if move_str.startswith(prefix):
            return name
    return ""


def resolve_opening(game: chess.pgn.Game) -> tuple[str, str]:
    """
    Return (opening_name, eco) for a game, using PGN headers first then
    falling back to ECO lookup and move detection.
    """
    headers = game.headers
    opening = headers.get("Opening", "").strip()
    eco     = headers.get("ECO", "").strip()

    if not opening and eco:
        opening = eco_to_name(eco)
    if not opening:
        opening = detect_from_moves(game)

    return opening, eco
