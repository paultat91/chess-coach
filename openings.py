def eco_to_name(eco: str) -> str:
    """Return a human-readable opening name for an ECO code, or empty string."""
    if not eco:
        return ""
    # Try exact match first, then strip trailing variant character
    return ECO_NAMES.get(eco.strip(), ECO_NAMES.get(eco.strip()[:3], ""))


